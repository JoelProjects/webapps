package datamodel;

public class SchoolStatus
{
  private int year;
  private String semester;
  
  public int getYear()
  {
    return year;
  }
  
  public void setYear(int year)
  {
    this.year = year;
  }

  public String getSemester()
  {
    return semester;
  }
  
  public void setSemester(String semester)
  {
    this.semester = semester;
  }
}
